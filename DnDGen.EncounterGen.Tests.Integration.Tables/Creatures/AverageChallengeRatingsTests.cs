        [Test]
        public void AllNoncombatantsHaveChallengeRatingOfZero()
        {
            var entries = GetEntries();
            var noncombatants = entries.Where(e => e.Contains(CreatureDataConstants.Noncombatant));

            foreach (var noncombatant in noncombatants)
            {
                var challengeRating = GetCollection(noncombatant).Single();
                Assert.That(challengeRating, Is.EqualTo(ChallengeRatingConstants.Zero), noncombatant);
            }
        }

        [Test]
        public void OnlyNoncombatantsHaveChallengeRatingOfZero()
        {
            var entries = GetEntries();
            var combatants = entries.Where(e => !e.Contains(CreatureDataConstants.Noncombatant));

            foreach (var combatant in combatants)
            {
                var challengeRating = GetCollection(combatant).Single();
                Assert.That(challengeRating, Is.Not.EqualTo(ChallengeRatingConstants.Zero), combatant);
            }
        }
